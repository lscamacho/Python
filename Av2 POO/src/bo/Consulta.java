package bo;

public class Consulta {

		private String codigo;
		private String pacienteCpf; 
		private String tratamentoCodigo;
		private String detalhes;
		
		/**
		 * @return the codigo
		 */
		public String getCodigo() {
			return codigo;
		}
		/**
		 * @param codigo the codigo to set
		 */
		public void setCodigo(String codigo) {
			this.codigo = codigo;
		}
		/**
		 * @return the pacienteCpf
		 */
		public String getPacienteCpf() {
			return pacienteCpf;
		}
		/**
		 * @param pacienteCpf the pacienteCpf to set
		 */
		public void setPacienteCpf(String pacienteCpf) {
			this.pacienteCpf = pacienteCpf;
		}
		/**
		 * @return the tratamentoCodigo
		 */
		public String getTratamentoCodigo() {
			return tratamentoCodigo;
		}
		/**
		 * @param tratamentoCodigo the tratamentoCodigo to set
		 */
		public void setTratamentoCodigo(String tratamentoCodigo) {
			this.tratamentoCodigo = tratamentoCodigo;
		}
		/**
		 * @return the detalhes
		 */
		public String getDetalhes() {
			return detalhes;
		}
		/**
		 * @param detalhes the detalhes to set
		 */
		public void setDetalhes(String detalhes) {
			this.detalhes = detalhes;
		}
		
		
		
		
		
	
		
		
}
